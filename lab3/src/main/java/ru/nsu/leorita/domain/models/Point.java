package ru.nsu.leorita.domain.models;

public class Point {
    private Double lon;
    private Double lat;

    public Point(Double lon, Double lat) {
        this.lat = lat;
        this.lon = lon;
    }

    public Double getLat() {
        return lat;
    }

    public void setLat(Double lat) {
        this.lat = lat;
    }

    public Double getLon() {
        return lon;
    }

    public void setLon(Double lon) {
        this.lon = lon;
    }
}
